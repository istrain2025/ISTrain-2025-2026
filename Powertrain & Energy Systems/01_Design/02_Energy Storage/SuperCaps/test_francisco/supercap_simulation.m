% --- new figure: power & capacitor current ---
figure('Name', 'Power & Current Analysis');

% 1. calculate power vector for plotting
% power = motor voltage (col 3) * braking current * 2 motors
power_motor_vector = results(:,3) .* brakingcurrent .* 2;

% subplot 1: power generated by motors
subplot(2, 1, 1);
plot(results(:,1), power_motor_vector, 'm', 'LineWidth', 2);
title('Total Power Generated by Motors (Pre-Efficiency)');
xlabel('Time (s)');
ylabel('Power (Watts)');
grid on;
legend('P_{generated} = V_{emf} * I_{brake} * 2');

% subplot 2: current going into supercapacitor
subplot(2, 1, 2);
