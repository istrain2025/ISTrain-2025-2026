title('Actual Current Entering Supercapacitor');
xlabel('Time (s)');
ylabel('Current (Amps)');
grid on;
legend('I_{cap} (Drops as Voltage Drops)');

% --- final results ---
% the 'end' keyword grabs the very last entry in the array
final_cap_voltage = results(end, 4);





figure('Name', 'Phase 2: Driving Range');

% Subplot 1: Speed
subplot(3,1,1);
plot(results_drive(:,4), results_drive(:,2), 'b', 'LineWidth', 2);
% Find the row index where Mode 3 starts
idx_coast = find(results_drive(:,5)==3, 1);

% Get the distance (Column 4) from that specific row
if ~isempty(idx_coast)
    xline(results_drive(idx_coast, 4), '--k', 'Power Cut');
end
title(['Total Distance: ' num2str(round(distance_total)) ' m']);
ylabel('Speed (m/s)'); xlabel('Distance (m)');
legend('Speed', 'Start of Coasting');
grid on;

% Subplot 2: Voltage Supply vs. Demand
subplot(3,1,2);

% Plot Capacitor Voltage (Column 3) - The Supply
plot(results_drive(:,4), results_drive(:,3), 'r', 'LineWidth', 2); hold on;

% Plot Motor Back-EMF (Column 6) - The Demand
plot(results_drive(:,4), results_drive(:,6), 'b--', 'LineWidth', 1.5);

% Plot the Limit Line
yline(cap_pre_charge, '--k', 'Empty Limit');

title('Voltage Convergence: Supply (Red) vs Demand (Blue)');
ylabel('Voltage (V)'); xlabel('Distance (m)');
legend('Capacitor Supply', 'Motor Back-EMF', 'Empty Limit');
